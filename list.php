<p>A computer program is a list of instructions that tell a computer what to do. 
    Everything a computer does is done by using a computer program. Programs stored in the memory of a computer let the computer do one thing after another, even with breaks in between.</p>
<p>There are types pf programming language:</p>

    <ul>
    <li>Procedural Programming Language</li>
    <li>Functional Programming Language</li>
    <li>Scripting Programming Language</li>
    <li>Logic Programming Language</li>
    <li>Object-Oriented Programming Language</li>
</ul>